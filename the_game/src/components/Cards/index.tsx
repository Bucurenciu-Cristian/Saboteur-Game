export const giveMeACard = (cards: Created[]) => {
  const random = Math.floor(Math.random() * cards.length);
  const created = cards[random];
  return created;
}
const giveMeTheFirstCards = (cards: Created[], NoPlayers: number, NoOfCards: number) => {
  const firstCards = [];
  for (let i = 0; i < NoPlayers * NoOfCards; i++) {
    firstCards.push(giveMeACard(cards));
  }
  return firstCards;
}

export const shuffleCards = (cards: Created[]) => {
  const newArray = [...cards]
  const length = newArray.length
  for (let start = 0; start < length; start++) {
    const randomPosition = Math.floor((newArray.length - start) * Math.random())
    const randomItem = newArray.splice(randomPosition, 1)

    newArray.push(...randomItem)
  }
  return newArray
}
const tellMeNoPlayers = (NoPlayers: number) => {
  switch (NoPlayers) {
    case 3:
      return [
        ...GoodGuys.slice(0, 3),
        ...BadGuys.slice(0, 1)
      ]
    case 4:
      return [
        ...GoodGuys.slice(0, 4),
        ...BadGuys.slice(0, 1)
      ]
    case 5:
      return [
        ...GoodGuys.slice(0, 4),
        ...BadGuys.slice(0, 2),
      ]
    case 6:
      return [
        ...GoodGuys.slice(0, 5),
        ...BadGuys.slice(0, 2),
      ]
    case 7:
      return [
        ...GoodGuys.slice(0, 5),
        ...BadGuys.slice(0, 3),
      ]
    case 8:
      return [
        ...GoodGuys.slice(0, 6),
        ...BadGuys.slice(0, 3),
      ]
    case 9:
      return [
        ...GoodGuys,
        ...BadGuys.slice(0, 3),
      ]
    case 10:
      return [
        ...GoodGuys,
        ...BadGuys,
      ]
    default:
      return [
        ...GoodGuys.slice(0, 1),
        ...BadGuys.slice(0, 1),
      ]
  }
}
